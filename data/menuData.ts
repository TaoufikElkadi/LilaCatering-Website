// Menu data for Catering Lila 2026.
// Source of truth: "Menukaart Catering Lila 2026 met prijzen.docx".
//
// Pricing model: the menu starts at a per-person BASE_PRICE (see lib/pricing.ts).
// Dishes WITHOUT a listed price in the document are `included` (surcharge 0) and
// are part of the base package. Dishes WITH a listed price add their `surcharge`
// per person on top of the base price. One item (the tyaffer tea ceremony) is a
// one-time `flatFee` rather than a per-person surcharge.

export type MenuCategory = 'appetizer' | 'starter' | 'main' | 'dessert';

export type MenuSubCategory =
  // appetizer
  | 'warm'
  | 'luxe'
  // starter (voorgerechten)
  | 'salad'
  | 'soup'
  | 'pastilla'
  // main (hoofdgerechten)
  | 'meat'
  | 'chicken'
  | 'fish'
  | 'buffet'
  | 'couscous';

export interface MenuItem {
  id: string;
  name: string;
  description: string;
  /** Euro amount added on top of BASE_PRICE. 0 when the dish is included in the base package. */
  surcharge: number;
  /** True when the document lists no price for the dish (part of the base package). */
  included: boolean;
  /** True when `surcharge` is a one-time fee instead of a per-person amount. */
  flatFee?: boolean;
  image: string;
  category: MenuCategory;
  subCategory?: MenuSubCategory;
  // Dietary and allergen information
  allergens?: string[];
  dietaryTags?: string[];
  ingredients?: string[];
  servingSize?: string;
  culturalInfo?: string;
}

