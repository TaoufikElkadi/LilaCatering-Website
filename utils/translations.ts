export function getTranslation(lang: Lang, path: string): any {
  const parts = path.split('.');
  let current: any = translations[lang];
  for (const part of parts) {
    if (current && part in current) {
      current = current[part];
    } else {
      current = undefined;
      break;
    }
  }
  if (current !== undefined) return current;

  // Fallback to English if missing
  current = translations.en;
  for (const part of parts) {
    if (current && part in current) {
      current = current[part];
    } else {
      return '';
    }
  }
  return current;
}

export function getAvailableLanguages(): Lang[] {
  return Object.keys(translations) as Lang[];
}

export function getLanguageName(lang: Lang): string {
  const names = {
    en: 'English',
    fr: 'Français',
    nl: 'Nederlands'
  };
  return names[lang] || lang;
}

export function getTranslatedMenuItem(lang: Lang, itemId: string): { 
  name: string; 
  description: string;
  ingredients?: string[];
  culturalInfo?: string;
  servingSize?: string;
} | null {
  const translation = getTranslation(lang, `menuBuilder.menuItems.${itemId}`);
  if (translation && translation.name) {
    return {
      name: translation.name,
      description: translation.description,
      ingredients: translation.ingredients,
      culturalInfo: translation.culturalInfo,
      servingSize: translation.servingSize,
    };
  }
  return null;
}
